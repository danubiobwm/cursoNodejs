// while
let dinheiro = 1000;

while (dinheiro > 0) {
  dinheiro = dinheiro - 100;
  console.log("posso gastar, tenho", dinheiro);
}

// Do / While

do {
  dinheiro = dinheiro - 100;
  console.log("posso gastar, tenho", dinheiro);
} while (dinheiro > 0);
console.log("Acabou  o seu Dinheiro", dinheiro);
